# Исходные данные 
R_earth = 6400000      
p0 = 101325            
g = 9.8                
mu = 0.029             
N_A = 6.022 * 10**23  

# масса атмосферы 
S_earth = 4 * 3.14 * R_earth**2         
M0 = (p0 * S_earth) / g                    

# число молей в атмосфере
moles = M0 / mu                              

# полное число молекул
N = moles * N_A                            

print(N)